// Bakes the exact Lucide icons the builder uses into src/icons.ts as inline SVG
// inner-markup — so the builder renders real icons with NO runtime dependency
// (lucide-static is only the dev-time source of truth). Re-run after editing the
// USED map. Source: Lucide (ISC/MIT). Run: node scripts/gen-icons.mjs
